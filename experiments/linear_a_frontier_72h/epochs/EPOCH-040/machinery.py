#!/usr/bin/env python3
"""
EPOCH-040 machinery — INTRA-WORD SIGN REPETITION (reduplication vs anti-repetition; L2/L3).

Pure L2/L3 DISTRIBUTIONAL statistic on ANONYMOUS sign ids. No phonetics, no sound, no
meaning, no reading. "repetition" = the same sign appearing >=2 times within a single
word (a distributional statistic), NOT a claim about sound or morpheme meaning.

FROZEN METRIC:
  rep_rate = fraction of words (len>=2) containing >=1 repeated sign.

FROZEN NULL (i.i.d.-unigram, LENGTH-MATCHED):
  For each observed word of length L, draw L signs i.i.d. from the GLOBAL unigram
  frequency distribution. Compute rep_rate over the synthetic corpus. >=2000 draws.
  TWO-SIDED p for deviation; direction = excess (reduplication) / deficit (avoidance).

POSITIVE CONTROL (gates verdict) on LB (DAMOS):
  (a) DETECT — plant a known repetition EXCESS (inject reduplicated words) and confirm
      the null detects excess (p<=0.05, correct direction); AND plant a known DEFICIT
      (remove all repeat-words) and confirm it detects deficit.
  (b) FALSE-POSITIVE — on words generated BY the i.i.d.-unigram model itself (true H0),
      rejection rate <=0.10 across >=20 sets.
  Failure of either -> MACHINERY_UNINFORMATIVE.

Self-check: `python3 machinery.py` runs the full pipeline + PC and prints JSON.
"""
